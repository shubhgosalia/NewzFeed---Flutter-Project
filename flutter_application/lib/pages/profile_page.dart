  _fetch() async {
    final firebaseUser = await FirebaseAuth.instance.currentUser!;
    await FirebaseFirestore.instance
        .collection('UserData')
        .doc(firebaseUser.uid)
        .get()
        .then((ds) {
      myEmail = ds.data()?['email'];
      print(myEmail);
    }).catchError((e) {
      print(e);
    });
  }

  _getuserdetails() async {
